% ============================================================
% ЛАБОРАТОРНА РОБОТА 8
% Дослідження алгоритму нечіткої кластеризації (FCM)
% ============================================================
%
% Завдання: Класифікація програмних модулів за метриками якості коду
% (кількість рядків коду, цикломатична складність) на кластери
% для автоматичного виявлення "проблемних" модулів.
% ============================================================

%% --- Генерація вхідних даних ---
% Імітуємо метрики програмних модулів:
%   Стовпець 1: кількість рядків коду (LOC), нормована [0..1]
%   Стовпець 2: цикломатична складність (CC), нормована [0..1]

rng(42); % фіксуємо генератор для відтворюваності

% Три групи модулів: прості, середні, складні
group1 = [0.1 + 0.15*randn(30,1), 0.1 + 0.1*randn(30,1)];  % прості
group2 = [0.5 + 0.12*randn(30,1), 0.5 + 0.1*randn(30,1)];  % середні
group3 = [0.8 + 0.08*randn(30,1), 0.8 + 0.1*randn(30,1)];  % складні

fcmdata = [group1; group2; group3];

% Обмежуємо значення в [0, 1]
fcmdata = max(0, min(1, fcmdata));

%% --- Завдання 2: FCM-кластеризація через командний рядок ---
% Виклик fcm з додатковими параметрами:
%   options(1) = 2.0  — показник ступеня для матриці U
%   options(2) = 100  — максимальна кількість ітерацій
%   options(3) = 1e-5 — мінімально допустима зміна цільової функції
%   options(4) = 1    — відображення інформації на кожному кроці

cluster_n = 3; % кількість кластерів
options = [2.0, 100, 1e-5, 1];

[center, U, obj_fcn] = fcm(fcmdata, cluster_n, options);

%% --- Завдання 3: Знаходження центрів і розподіл за кластерами ---

% Визначаємо максимальну ступінь приналежності для кожної точки
maxU = max(U);

index1 = find(U(1,:) == maxU);  % індекси точок кластеру 1
index2 = find(U(2,:) == maxU);  % індекси точок кластеру 2
index3 = find(U(3,:) == maxU);  % індекси точок кластеру 3

%% --- Figure 1: Розподіл точок за кластерами ---
figure('Name', 'FCM: Розподіл програмних модулів за кластерами');

plot(fcmdata(index1,1), fcmdata(index1,2), 'bo', ...
     'MarkerSize', 6, 'LineWidth', 1.2, 'DisplayName', 'Прості модулі');
hold on;
plot(fcmdata(index2,1), fcmdata(index2,2), 'rs', ...
     'MarkerSize', 6, 'LineWidth', 1.2, 'DisplayName', 'Середні модулі');
plot(fcmdata(index3,1), fcmdata(index3,2), 'g^', ...
     'MarkerSize', 6, 'LineWidth', 1.2, 'DisplayName', 'Складні модулі');

% Центри кластерів
plot(center(1,1), center(1,2), 'bo', 'MarkerSize', 15, 'LineWidth', 3, ...
     'DisplayName', 'Центр 1');
plot(center(2,1), center(2,2), 'rs', 'MarkerSize', 15, 'LineWidth', 3, ...
     'DisplayName', 'Центр 2');
plot(center(3,1), center(3,2), 'g^', 'MarkerSize', 15, 'LineWidth', 3, ...
     'DisplayName', 'Центр 3');
hold off;

xlabel('LOC (кількість рядків коду, норм.)');
ylabel('CC (цикломатична складність, норм.)');
title('FCM-кластеризація програмних модулів');
legend('Location', 'northwest');
grid on;

%% --- Figure 2: Графік зміни цільової функції ---
figure('Name', 'FCM: Зміна цільової функції');

plot(obj_fcn, 'k-o', 'LineWidth', 2, 'MarkerSize', 5);
xlabel('Номер ітерації');
ylabel('Значення цільової функції');
title('Динаміка зміни цільової функції FCM');
grid on;

%% --- Виведення результатів у командне вікно ---
fprintf('\n=== Центри кластерів ===\n');
for j = 1:cluster_n
    fprintf('Кластер %d: LOC = %.4f, CC = %.4f\n', j, center(j,1), center(j,2));
end

fprintf('\n=== Кількість модулів у кластерах ===\n');
fprintf('Кластер 1 (Прості):  %d модулів\n', length(index1));
fprintf('Кластер 2 (Середні): %d модулів\n', length(index2));
fprintf('Кластер 3 (Складні): %d модулів\n', length(index3));
fprintf('Всього ітерацій: %d\n', length(obj_fcn));
fprintf('Фінальне значення цільової функції: %.4f\n', obj_fcn(end));
